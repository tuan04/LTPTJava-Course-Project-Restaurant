        // Chuyển đổi java.util.Date sang java.time.LocalDate
        LocalDate ngaySinh = ngaySinhDate.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();

        // Tạo đối tượng NhanVien với dữ liệu cập nhật
        NhanVien updateNV = new NhanVien(ma, ten, cCCD, sdt, trangThai, new LoaiNhanVien(loaiNhanVien), ngaySinh, gtinh, mailnv);

        // Gọi phương thức cập nhật trong DAO để lưu vào DB
        if (dao_nv.CapNhatNhanVien(updateNV)) {
            JOptionPane.showMessageDialog(this, "Cập nhật thành công!");

            // Cập nhật lại dữ liệu trên bảng
            int selectedRow = table.getSelectedRow();
            if (selectedRow != -1) {
                tbm.setValueAt(ten, selectedRow, 1);
                tbm.setValueAt(cCCD, selectedRow, 2);
                tbm.setValueAt(sdt, selectedRow, 3);
                tbm.setValueAt(trangThai ? "Đang làm" : "Đã nghỉ", selectedRow, 5);
                tbm.setValueAt(loaiNhanVien, selectedRow, 6);
                if (ngaySinh != null) {
                    tbm.setValueAt(new SimpleDateFormat("yyyy-MM-dd").format(ngaySinh), selectedRow, 7);
                }
                tbm.setValueAt(gtinh ? "Nam" : "Nữ", selectedRow, 8);
                tbm.setValueAt(mailnv, selectedRow, 9);
            }

            // Làm mới bảng và đóng form
            parentPanel.refreshTable();
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Cập nhật thất bại.");
        }
