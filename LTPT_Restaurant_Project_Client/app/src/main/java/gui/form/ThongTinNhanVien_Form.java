    public void setData(String ma) {
        NhanVien nv = dao_nv.getNV(ma);
        LoaiNhanVien lNV = dao_lnv.TimLoaiNhanVien(nv.getLoaiNhanVien().getMaLoaiNV());
        manv.setText(nv.getMaNV());
        tennv.setText(nv.getHoTenNV());
        cccd.setText(nv.getCCCD());
        sdtnv.setText(nv.getSoDienThoai());
        loainv.setText(lNV.getViTri());
        if (nv.getNgaySinh() != null) {
            nsinh.setDate(java.sql.Date.valueOf(nv.getNgaySinh()));
        }

        if (nv.isTrangThai()) {
            conlam.setSelected(true);

        } else {
            danghi.setSelected(true);

        }

        if (nv.isGioiTinh()) {
            nu.setSelected(true);
        } else {
            nam.setSelected(true);
        }
        mail.setText(nv.getEmail());

    }

