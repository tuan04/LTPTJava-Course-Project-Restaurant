    public void duyetMonVaoComboxBox() {
        ArrayList<LoaiMonAn> list = dao_ma.getListLoai();
        for (LoaiMonAn m : list) {
            int flag = -1;
            String loai = m.getTenLoaiMA();
            for (int i = 0; i < cbbmon.getItemCount(); i++) {
                if (loai.equals(cbbmon.getItemAt(i))) {
                    flag = 1;
                }
            }
            if (flag == -1) {
                cbbmon.addItem(loai);
            }
            flag = 1;
        }
    }

