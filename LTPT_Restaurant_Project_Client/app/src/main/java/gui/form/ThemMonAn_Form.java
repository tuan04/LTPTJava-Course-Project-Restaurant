    public static void saveImageToSoftware(File imageFile) {
        try {
            // Thư mục lưu trữ hình ảnh trong phần mềm
            String savePath = "src/hinhAnh/";
            File destinationFolder = new File(savePath);
            
            // Tạo thư mục nếu chưa tồn tại
            if (!destinationFolder.exists()) {
                destinationFolder.mkdirs();
            }
            
            // Tên file mới
            File destinationFile = new File(destinationFolder, imageFile.getName());
            
            // Copy file
            java.nio.file.Files.copy(
                imageFile.toPath(),
                destinationFile.toPath(),
                java.nio.file.StandardCopyOption.REPLACE_EXISTING
            );
            
            System.out.println("Hình ảnh đã được lưu vào: " + destinationFile.getAbsolutePath());
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Lỗi khi lưu hình ảnh: " + e.getMessage());
        }
    }
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            saveImageToSoftware(selectedImageFile);
            String ma = dao_ma.maTuSinh();
            String hinhAnh = tenAnh;
            // Kiểm tra nếu đường dẫn ảnh được chọn
            if (hinhAnh == null || hinhAnh.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn ảnh!");
                return;
            }
            
            hinhAnh = "/hinhAnh/" + tenAnh;

            String ten = tenmon.getText().trim();
            if (ten.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Tên không được để trống!");
                return;
            }

            float pr;

            try {
                pr = Float.parseFloat(gia.getText().trim());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Giá tiền không hợp lệ!");
                return;
            }
            String tenLoaiMon = cbbmon.getSelectedItem().toString();

            LoaiMonAn loaiMon = loaiMon_DAO.TimLoaiMonTheoTen(tenLoaiMon);

            KhuyenMai defaultKm = new KhuyenMai();

            MonAn emp = new MonAn(ma, ten, hinhAnh, pr, true, loaiMon, defaultKm);

            if (dao_ma.ThemMonAn(emp)) {
                tbm.setRowCount(0);
                duyetListVaoTable();
                JOptionPane.showMessageDialog(this, "Thêm thành công!");
                // Đóng form sau khi cập nhật
                parentPanel.refreshTable();
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Thêm không thành công!");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

