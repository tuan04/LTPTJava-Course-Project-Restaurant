    private void button3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button3ActionPerformed
        // TODO add your handling code here:
        String relativePath = "src/hinhAnh";
        File currentDir = new File(System.getProperty("user.dir"), relativePath);

        // Khởi tạo JFileChooser với thư mục tương đối
        JFileChooser browseImageFile = new JFileChooser(currentDir);
        //
        FileNameExtensionFilter fnef = new FileNameExtensionFilter("IMAGES", "png", "jpg", "jpeg");
        browseImageFile.addChoosableFileFilter(fnef);

        int showOpenDialogue = browseImageFile.showOpenDialog(null);
        if (showOpenDialogue == JFileChooser.APPROVE_OPTION) {
            selectedImageFile = browseImageFile.getSelectedFile();
            
            selectedImagePath = selectedImageFile.getAbsolutePath();
            tenAnh = selectedImageFile.getName();
            JOptionPane.showMessageDialog(null, selectedImagePath);
            //
            ImageIcon ii = new ImageIcon(selectedImagePath);
            //
            Image image = ii.getImage().getScaledInstance(ha.getWidth(), ha.getHeight(), Image.SCALE_SMOOTH);
            ha.setIcon(new ImageIcon(image));
        }
    }//GEN-LAST:event_button3ActionPerformed

