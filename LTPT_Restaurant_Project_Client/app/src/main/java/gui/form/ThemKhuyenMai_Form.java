    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(checkValue()){
            System.out.println("ok");
        try {
            SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");
            String tenKM = txtTenKM.getText();
            int giamGia = Integer.parseInt(txtGiamGia.getText());
            System.out.println(txtSoLuong.getText());
            int soLuong = txtSoLuong.getText() != null && !txtSoLuong.getText().isEmpty() 
              ? Integer.parseInt(txtSoLuong.getText()) 
              : 0;
            String loaiKM = cmbLoaiKM.getSelectedItem().toString().trim().equalsIgnoreCase("Hóa đơn") ? "HoaDon" : "MonAn";

            // Lấy LocalDate từ JDateChooser
            LocalDate ngayBD = jDateNgayBD.getDate().toInstant()
                                 .atZone(ZoneId.systemDefault()).toLocalDate();
            LocalDate ngayHH = jDateNgayHH.getDate().toInstant()
                                 .atZone(ZoneId.systemDefault()).toLocalDate();

            String maKM = taoMaKM();

            // Tạo đối tượng KhuyenMai
            KhuyenMai km = new KhuyenMai(maKM, tenKM, giamGia, ngayHH, ngayBD, soLuong, loaiKM);

            // Gọi DAO để thêm khuyến mãi
            Boolean check = dao_km.themKhuyenMai(km);

            if (loaiKM.equals("HoaDon")) {
                if (check) {
                    JOptionPane.showMessageDialog(this, "Thêm thành công");
                    addTable(km);
                    cmbLoaiKM.setSelectedIndex(0);
                    parentPanel.refreshTable();
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm thất bại");
                }
            } else {
                if (check) {
                    System.out.println("OK");
                    JOptionPane.showMessageDialog(this, "Thêm món cho khuyến mãi");
                    DanhSachMonAn form = new DanhSachMonAn(km.getMaKM());
                    form.setVisible(true);
                    addTable(km);
                    parentPanel.refreshTable();
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm thất bại");
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

