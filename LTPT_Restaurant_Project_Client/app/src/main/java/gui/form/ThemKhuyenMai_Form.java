    public void addTable(KhuyenMai km){
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        Object[] obj = new Object[7];
        obj[0] = km.getMaKM();
        obj[1] = km.getTenKM();
        obj[2] = km.getGiamGia();
        obj[3] = km.getNgayBD();
        obj[4] = km.getNgayHH();
        obj[5] = km.getSoLuong();
        obj[6] = km.getLoaiKM();
        model.addRow(obj);

    }
